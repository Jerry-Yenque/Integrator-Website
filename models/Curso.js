class Curso {
    constructor(
        id, codigo, nombre, semanas, diasPorSemana, ultimaClase
    ) {
        this.id = id;
        this.codigo = codigo;
        this.nombre = nombre;
        this.semanas = semanas;
        this.diasPorSemana = diasPorSemana;
        this.ultimaClase = ultimaClase;
    }
}

export default Curso;